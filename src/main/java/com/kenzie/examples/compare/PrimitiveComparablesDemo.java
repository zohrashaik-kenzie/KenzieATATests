package com.kenzie.examples.compare;

public class PrimitiveComparablesDemo {

    public static void main(String[] args){
        System.out.println(1 == 2); // outputs false
        System.out.println(3 > 1); // outputs true
        System.out.println(1 != 1); // outputs false
        System.out.println(10 >= 9); // outputs true
    }
}
